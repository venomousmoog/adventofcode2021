def compute(data):
    map = data.split('\n')
    h = len(map)
    w = len(map[0])

    # make a big list of cucumber positions - ordered to the right and down
    cucumbers = {}
    for (ri, r) in enumerate(map):
        for (ci, c) in enumerate(r):
            if c != empty:
                cucumbers[(ri, ci)] = c

    print(cucumbers)

    def isempty(cuc, ri, ci):
        return not (ri, ci) in cuc

    def update(cuc, dv):
        s, dr, dc = dv
        u = {}
        for ((r, c), d) in cuc.items():
            if d == s and isempty(cuc, (r+dr)%h, (c+dc)%w):
                u[((r+dr)%h, (c+dc)%w)] = d
            else:
                u[(r, c)] = d
        return u

    def output(cuc):
        # print(cuc)
        o = [list([empty for _ in range(w)]) for r in range(h)]
        # print(o)
        for ((r, c), d) in cuc.items():
            # print(f'd={d} r={r} c={c}')
            o[r][c] = d
        # print(o)
        for r in o:
            for c in r:
                print(c, end="")
            print()

    output(cucumbers)
    print()

    ed=('>', +0, +1)
    sd=('v', +1, +0)
    iteration = 0
    while True:
        iteration = iteration + 1
        nf = update(cucumbers, ed)
        nf = update(nf, sd)
        if set(nf) == set(cucumbers):
            break
        cucumbers = nf

    output(cucumbers)
    print(f'iterations = {iteration}')


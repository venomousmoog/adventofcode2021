def compute(data):
    lines = data.split('\n')

    pairs = {
        '(':')',
        '[':']',
        '{':'}',
        '<':'>',
    }

    p1_scores = {
        ')':3,
        ']':57,
        '}':1197,
        '>':25137,
    }

    p2_scores = {
        ')':1,
        ']':2,
        '}':3,
        '>':4,
    }

    p1_score = 0
    p2_score = []
    for l in lines:
        stack = []
        corrupt = False
        for c in l:
            if c in pairs.keys():
                stack.append(c)
            elif c in pairs.values():
                m = stack.pop()
                if pairs[m] != c:
                    # print(f'corrupted: {l}')
                    # print(f"{m} doesn't match {c} expected {pairs[m]}")
                    p1_score += p1_scores[c]
                    corrupt = True
                    break
        if len(stack) > 0 and not corrupt:
            print(stack)
            # complete the braces - inverse of the stack:
            stack.reverse()
            completion_cost = 0
            for c in stack:
                completion_cost = completion_cost * 5
                completion_cost += p2_scores[pairs[c]]
            print(f'completion score for {l} is {completion_cost} from {stack}')
            p2_score.append(completion_cost)

    p2_score.sort()
    print(f'p1 = {p1_score}')
    middle = int(len(p2_score)/2)
    print(p2_score)
    print(f'middle of {len(p2_score)} is {middle}')
    print(f'p2 = {p2_score[middle]}')


